using Content.Client.UserInterface.Controls;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;
using Robust.Client.UserInterface;
using System.Numerics;

namespace Content.Client._RMC14.Xenonids.Ping;

[GenerateTypedNameReferences]
public sealed partial class XenoPingMenu : Control
{
    private readonly ColoredSimpleRadialMenu _radialMenu;

    public XenoPingMenu()
    {
        _radialMenu = new ColoredSimpleRadialMenu();
        AddChild(_radialMenu);
        RobustXamlLoader.Load(this);
    }

    public void Track(EntityUid owner) => _radialMenu.Track(owner);

    public void SetButtons(IEnumerable<RadialMenuOption> models, SimpleRadialMenuSettings? settings = null)
        => _radialMenu.SetButtons(models, settings);

    public void OpenOverMouseScreenPosition() => _radialMenu.OpenOverMouseScreenPosition();

    public void Close() => _radialMenu.Close();

    public void OpenCenteredAt(Vector2 position) => _radialMenu.OpenCenteredAt(position);
}
