using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._RMC14.Admin;

[GenerateTypedNameReferences]
public sealed partial class RMCSquadRow : BoxContainer
{
    public RMCSquadRow()
    {
        RobustXamlLoader.Load(this);
    }

    public void CreateSquadButton(bool exists, Action pressed, int? members, string name, Color color)
    {
        if (exists)
        {
            Members.Text = members == null ? null : Loc.GetString("rmc-ui-members", ("members", members));
            CreateButton.Disabled = true;
        }
        else
        {
            CreateButton.OnPressed += _ => pressed.Invoke();
        }

        CreateButton.Text = name;

        if (exists)
            CreateButton.ModulateSelfOverride = color;
    }
}
