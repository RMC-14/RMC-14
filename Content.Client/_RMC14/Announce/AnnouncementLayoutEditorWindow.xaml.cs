namespace Content.Client._RMC14.Announce;

[GenerateTypedNameReferences]
public sealed partial class AnnouncementLayoutEditorWindow : DefaultWindow
{
    [Dependency] private readonly IConfigurationManager _cfg = default!;
    [Dependency] private readonly IPrototypeManager _prototypeManager = default!;
    [Dependency] private readonly ISerializationManager _serialization = default!;
    [Dependency] private readonly IEntityManager _entityManager = default!;
    [Dependency] private readonly IUserInterfaceManager _uiManager = default!;
    [Dependency] private readonly IPlayerManager _player = default!;

    private readonly AnnouncementControllerSystem _announcementController;
    private readonly AnnouncementLayoutEditorUIController _layoutController;
    private readonly List<AnnouncementPresetPrototype> _presets;
    private readonly List<string> _presetLabels;
    private ProtoId<AnnouncementPresetPrototype> _selectedPreset;
    private AnnouncementDisplayPreference _selectedPreviewPreference = AnnouncementDisplayPreference.Stylized;
    private bool _suppressSliderChange;
    private bool _suppressTextChange;

    public AnnouncementLayoutEditorWindow()
    {
        SetSize = new Vector2i(760, 460);
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        _announcementController = _entityManager.System<AnnouncementControllerSystem>();
        _layoutController = _uiManager.GetUIController<AnnouncementLayoutEditorUIController>();
        _presets = GetEditablePresets();
        _presetLabels = BuildPresetLabels(_presets);
        _selectedPreset = _presets.Count > 0 ? _presets[0].ID : string.Empty;

        ConfigurePresetButton();
        ConfigureVariantButton();
        ConfigureNumericInputs();
        ConfigureAppearanceInputs();

        ScaleSlider.OnValueChanged += _ =>
        {
            if (_suppressSliderChange)
                return;

            SaveEditedLayout(CreateEditedLayout(GetPreviewPositionOrDefault(), ScaleSlider.Value));
            RefreshPreview();
        };

        ResetTargetButton.OnPressed += _ =>
        {
            ResetEditedLayout();
            RefreshPreview();
        };

        ResetAllButton.OnPressed += _ =>
        {
            _cfg.SetCVar(RMCCVars.RMCAnnouncementLayout, string.Empty);
            _cfg.SetCVar(RMCCVars.RMCAnnouncementLayoutOverrides, string.Empty);
            RefreshPreview();
        };

        OnClose += () =>
        {
            _layoutController.HidePreview();
            _layoutController.PreviewPositionChanged -= OnControllerPreviewPositionChanged;
            _layoutController.PreviewScaleChanged -= OnControllerPreviewScaleChanged;
        };

        _layoutController.PreviewPositionChanged += OnControllerPreviewPositionChanged;
        _layoutController.PreviewScaleChanged += OnControllerPreviewScaleChanged;
        RefreshPreview();
    }

    private void ConfigurePresetButton()
    {
        for (var i = 0; i < _presets.Count; i++)
        {
            PreviewPresetButton.AddItem(_presetLabels[i], i);
        }

        PreviewPresetButton.OnItemSelected += args =>
        {
            PreviewPresetButton.SelectId(args.Id);
            var preset = _presets[args.Id];
            _selectedPreset = preset.ID;
            _selectedPreviewPreference = ResolveLivePreviewPreference(preset);
            RefreshVariantButton();
            RefreshPreview();
        };

        if (_presets.Count > 0)
        {
            _selectedPreviewPreference = ResolveLivePreviewPreference(_presets[0]);
            PreviewPresetButton.SelectId(0);
            RefreshVariantButton();
        }
    }

    private void ConfigureVariantButton()
    {
        PreviewVariantButton.OnItemSelected += args =>
        {
            PreviewVariantButton.SelectId(args.Id);
            if (TryGetPreviewPreferenceForId(args.Id, out var preference))
            {
                _selectedPreviewPreference = preference;
                RefreshPreview();
            }
        };
    }

    private void ConfigureNumericInputs()
    {
        PositionXInput.OnTextEntered += args => TryCommitNumericInputs(args.Text);
        PositionYInput.OnTextEntered += args => TryCommitNumericInputs(args.Text);
        ScaleInput.OnTextEntered += args => TryCommitNumericInputs(args.Text);
        BodyScaleInput.OnTextEntered += args => TryCommitNumericInputs(args.Text);
        TitleScaleInput.OnTextEntered += args => TryCommitNumericInputs(args.Text);

        PositionXInput.OnFocusExit += args => TryCommitNumericInputs(args.Text);
        PositionYInput.OnFocusExit += args => TryCommitNumericInputs(args.Text);
        ScaleInput.OnFocusExit += args => TryCommitNumericInputs(args.Text);
        BodyScaleInput.OnFocusExit += args => TryCommitNumericInputs(args.Text);
        TitleScaleInput.OnFocusExit += args => TryCommitNumericInputs(args.Text);
    }

    private void ConfigureAppearanceInputs()
    {
        ShowTitleCheckBox.OnPressed += _ => CommitAppearanceInputs();
        ShowPortraitCheckBox.OnPressed += _ => CommitAppearanceInputs();

        BodyColorPicker.OnColorChanged += color => OnColorPickerChanged(BodyColorInput, color);
        TitleColorPicker.OnColorChanged += color => OnColorPickerChanged(TitleColorInput, color);
        SpriteBoxColorPicker.OnColorChanged += color => OnColorPickerChanged(SpriteBoxColorInput, color);
        SpriteBoxBorderColorPicker.OnColorChanged += color => OnColorPickerChanged(SpriteBoxBorderColorInput, color);
        CRTGlowColorPicker.OnColorChanged += color => OnColorPickerChanged(CRTGlowColorInput, color);
        BackgroundColorPicker.OnColorChanged += color => OnColorPickerChanged(BackgroundColorInput, color);

        BodyColorInput.OnTextEntered += args => CommitAppearanceInputs();
        TitleColorInput.OnTextEntered += args => CommitAppearanceInputs();
        SpriteBoxColorInput.OnTextEntered += args => CommitAppearanceInputs();
        SpriteBoxBorderColorInput.OnTextEntered += args => CommitAppearanceInputs();
        CRTGlowColorInput.OnTextEntered += args => CommitAppearanceInputs();
        BackgroundColorInput.OnTextEntered += args => CommitAppearanceInputs();

        BodyColorInput.OnFocusExit += args => CommitAppearanceInputs();
        TitleColorInput.OnFocusExit += args => CommitAppearanceInputs();
        SpriteBoxColorInput.OnFocusExit += args => CommitAppearanceInputs();
        SpriteBoxBorderColorInput.OnFocusExit += args => CommitAppearanceInputs();
        CRTGlowColorInput.OnFocusExit += args => CommitAppearanceInputs();
        BackgroundColorInput.OnFocusExit += args => CommitAppearanceInputs();
    }

    private void RefreshVariantButton()
    {
        PreviewVariantButton.Clear();

        if (!_prototypeManager.TryIndex<AnnouncementPresetPrototype>(_selectedPreset, out var preset))
            return;

        var availablePreferences = GetAvailablePreviewPreferences(preset);
        if (!availablePreferences.Contains(_selectedPreviewPreference))
        {
            var resolved = _announcementController.ResolveDisplayPreference(_selectedPreset);
            _selectedPreviewPreference = availablePreferences.Contains(resolved)
                ? resolved
                : availablePreferences[0];
        }

        for (var i = 0; i < availablePreferences.Count; i++)
        {
            var preference = availablePreferences[i];
            PreviewVariantButton.AddItem(GetPreferenceName(preference), i);
            if (preference == _selectedPreviewPreference)
                PreviewVariantButton.SelectId(i);
        }
    }

    private bool TryGetPreviewPreferenceForId(int id, out AnnouncementDisplayPreference preference)
    {
        preference = AnnouncementDisplayPreference.Stylized;
        if (!_prototypeManager.TryIndex<AnnouncementPresetPrototype>(_selectedPreset, out var preset))
            return false;

        var availablePreferences = GetAvailablePreviewPreferences(preset);
        if (id < 0 || id >= availablePreferences.Count)
            return false;

        preference = availablePreferences[id];
        return true;
    }

    private static List<AnnouncementDisplayPreference> GetAvailablePreviewPreferences(AnnouncementPresetPrototype preset)
    {
        var preferences = new List<AnnouncementDisplayPreference>();

        if (preset.Presentations.Stylized != null)
            preferences.Add(AnnouncementDisplayPreference.Stylized);

        if (preset.Presentations.Default != null)
            preferences.Add(AnnouncementDisplayPreference.Default);

        if (preset.Presentations.Simplified != null)
            preferences.Add(AnnouncementDisplayPreference.Simplified);

        if (preferences.Count == 0)
            preferences.Add(AnnouncementDisplayPreference.Stylized);

        return preferences;
    }

    private static string GetPreferenceName(AnnouncementDisplayPreference preference)
    {
        return Loc.GetString(preference switch
        {
            AnnouncementDisplayPreference.Stylized => "rmc-ui-options-announcements-style-stylized",
            AnnouncementDisplayPreference.Default => "rmc-ui-options-announcements-style-default",
            AnnouncementDisplayPreference.Simplified => "rmc-ui-options-announcements-style-simplified",
            _ => "rmc-ui-options-announcements-style-default"
        });
    }

    private AnnouncementDisplayPreference ResolveLivePreviewPreference(AnnouncementPresetPrototype preset)
    {
        var availablePreferences = GetAvailablePreviewPreferences(preset);
        var resolved = _announcementController.ResolveDisplayPreference(preset.ID);
        if (availablePreferences.Contains(resolved))
            return resolved;

        return availablePreferences[0];
    }

    private List<AnnouncementPresetPrototype> GetEditablePresets()
    {
        return _prototypeManager.EnumeratePrototypes<AnnouncementPresetPrototype>()
            .Where(ShouldIncludeInLayoutEditor)
            .OrderBy(preset => (string?)preset.GroupId ?? (string)preset.ID)
            .ThenBy(preset => preset.ID)
            .ToList();
    }

    private static bool ShouldIncludeInLayoutEditor(AnnouncementPresetPrototype preset)
    {
        return preset.VisibleInLayoutEditor;
    }

    private static List<string> BuildPresetLabels(IReadOnlyList<AnnouncementPresetPrototype> presets)
    {
        var duplicates = presets
            .GroupBy(preset => preset.Name)
            .Where(group => group.Count() > 1)
            .Select(group => group.Key)
            .ToHashSet();

        var labels = new List<string>(presets.Count);
        foreach (var preset in presets)
        {
            labels.Add(duplicates.Contains(preset.Name)
                ? $"{preset.Name} ({preset.ID})"
                : preset.Name);
        }

        return labels;
    }

    private void TryCommitNumericInputs(string _)
    {
        if (_suppressTextChange)
            return;

        if (!TryParseLayoutInputs(out var layout))
        {
            var currentLayout = GetEffectiveEditedLayout();
            RefreshValueLabels(currentLayout, GetStoredEditedLayout() != null);
            return;
        }

        SaveEditedLayout(layout);
        RefreshPreview();
    }

    private bool TryParseLayoutInputs(out AnnouncementLayoutOverride layout)
    {
        layout = default;

        if (!float.TryParse(PositionXInput.Text, NumberStyles.Float, CultureInfo.InvariantCulture, out var x) ||
            !float.TryParse(PositionYInput.Text, NumberStyles.Float, CultureInfo.InvariantCulture, out var y) ||
            !float.TryParse(ScaleInput.Text, NumberStyles.Float, CultureInfo.InvariantCulture, out var scale) ||
            !float.TryParse(BodyScaleInput.Text, NumberStyles.Float, CultureInfo.InvariantCulture, out var bodyScale) ||
            !float.TryParse(TitleScaleInput.Text, NumberStyles.Float, CultureInfo.InvariantCulture, out var titleScale))
        {
            return false;
        }

        layout = CreateEditedLayout(new Vector2(x, y), scale, bodyScale, titleScale);
        return true;
    }

    private AnnouncementLayoutOverride CreateEditedLayout(Vector2 position, float scale)
    {
        var effective = GetEffectiveEditedLayout();
        return CreateEditedLayout(
            position,
            scale,
            ParseScaleInput(BodyScaleInput.Text, effective?.BodyTextScale ?? 1f),
            ParseScaleInput(TitleScaleInput.Text, effective?.TitleTextScale ?? 1f));
    }

    private AnnouncementLayoutOverride CreateEditedLayout(
        Vector2 position,
        float scale,
        float bodyTextScale,
        float titleTextScale)
    {
        return new AnnouncementLayoutOverride(
            position,
            scale,
            ShowTitleCheckBox.Pressed,
            ShowPortraitCheckBox.Disabled ? null : ShowPortraitCheckBox.Pressed,
            ParseOptionalColorInput(BodyColorInput.Text),
            ParseOptionalColorInput(TitleColorInput.Text),
            bodyTextScale,
            titleTextScale,
            ParseOptionalColorInput(SpriteBoxColorInput.Text),
            ParseOptionalColorInput(SpriteBoxBorderColorInput.Text),
            ParseOptionalColorInput(CRTGlowColorInput.Text),
            ParseOptionalColorInput(BackgroundColorInput.Text)).Clamp();
    }

    private static float ParseScaleInput(string? value, float fallback = 1f)
    {
        return float.TryParse(value, NumberStyles.Float, CultureInfo.InvariantCulture, out var scale)
            ? scale
            : fallback;
    }

    private static string? ParseOptionalColorInput(string? value)
    {
        if (string.IsNullOrWhiteSpace(value))
            return null;

        var trimmed = value.Trim();
        return Color.TryFromHex(trimmed) != null ? trimmed : null;
    }

    private void CommitAppearanceInputs()
    {
        if (_suppressTextChange)
            return;

        SaveEditedLayout(CreateEditedLayout(GetPreviewPositionOrDefault(), ScaleSlider.Value));
        RefreshPreview();
    }

    private void OnColorPickerChanged(LineEdit targetInput, Color color)
    {
        if (_suppressTextChange)
            return;

        targetInput.Text = color.ToHex();
        CommitAppearanceInputs();
    }

    private void RefreshPreview()
    {
        if (_presets.Count == 0 ||
            !_prototypeManager.TryIndex<AnnouncementPresetPrototype>(_selectedPreset, out var preset))
        {
            PositionValueLabel.Text = Loc.GetString("rmc-ui-options-announcements-layout-no-presets");
            ScaleValueLabel.Text = Loc.GetString("rmc-ui-options-announcements-layout-scale-value", ("value", "1.00x"));
            return;
        }

        var availablePreferences = GetAvailablePreviewPreferences(preset);
        if (!availablePreferences.Contains(_selectedPreviewPreference))
            _selectedPreviewPreference = availablePreferences[0];
        UpdatePresetModesLabel(availablePreferences);

        var previewData = new AnnouncementNetData
        {
            AnnouncementId = preset.ID,
            Text = GetPreviewBody(preset),
            SpeakerName = ResolvePreviewSpeakerName()
        };

        if (_player.LocalEntity is { } localEntity)
            previewData.SpeakerEntity = _entityManager.GetNetEntity(localEntity);

        if (!AnnouncementDisplayResolver.TryResolve(_prototypeManager, _serialization, previewData, _selectedPreviewPreference, out var display))
            return;

        UpdateAppearanceControlState(display);

        var editableLayout = GetEffectiveEditedLayout();
        if (editableLayout is { } layout)
            AnnouncementLayoutResolver.Apply(display, layout);

        RefreshColorPickers(display);

        _suppressSliderChange = true;
        ScaleSlider.SetValueWithoutEvent(editableLayout?.Scale ?? 1f);
        _suppressSliderChange = false;

        RefreshValueLabels(editableLayout, editableLayout != null);
        _layoutController.ShowPreview(display);

        ResetTargetButton.Disabled = GetStoredEditedLayout() == null;
    }

    private void RefreshValueLabels(AnnouncementLayoutOverride? layout, bool hasCustomLayout)
    {
        var position = layout?.ScreenPosition;
        var scale = layout?.Scale ?? 1f;
        ScaleValueLabel.Text = Loc.GetString(
            "rmc-ui-options-announcements-layout-scale-value",
            ("value", $"{scale:0.00}x"));

        _suppressTextChange = true;
        PositionXInput.Text = (position?.X ?? 0.5f).ToString("0.000", CultureInfo.InvariantCulture);
        PositionYInput.Text = (position?.Y ?? 0.18f).ToString("0.000", CultureInfo.InvariantCulture);
        ScaleInput.Text = scale.ToString("0.000", CultureInfo.InvariantCulture);
        BodyScaleInput.Text = (layout?.BodyTextScale ?? 1f).ToString("0.000", CultureInfo.InvariantCulture);
        TitleScaleInput.Text = (layout?.TitleTextScale ?? 1f).ToString("0.000", CultureInfo.InvariantCulture);
        ShowTitleCheckBox.Pressed = layout?.ShowTitle ?? true;
        ShowPortraitCheckBox.Pressed = ShowPortraitCheckBox.Disabled ? false : layout?.ShowSprite ?? true;
        BodyColorInput.Text = layout?.TextColor ?? string.Empty;
        TitleColorInput.Text = layout?.TitleColor ?? string.Empty;
        SpriteBoxColorInput.Text = layout?.SpriteBoxColor ?? string.Empty;
        SpriteBoxBorderColorInput.Text = layout?.SpriteBoxBorderColor ?? string.Empty;
        CRTGlowColorInput.Text = layout?.CRTGlowColor ?? string.Empty;
        BackgroundColorInput.Text = layout?.BackgroundColor ?? string.Empty;
        _suppressTextChange = false;

        PositionValueLabel.Text = hasCustomLayout && position is { } value
            ? Loc.GetString(
                "rmc-ui-options-announcements-layout-position-value",
                ("x", value.X.ToString("0.000")),
                ("y", value.Y.ToString("0.000")))
            : Loc.GetString("rmc-ui-options-announcements-layout-position-default");
    }

    private void UpdatePresetModesLabel(IReadOnlyCollection<AnnouncementDisplayPreference> preferences)
    {
        var names = preferences.Select(GetPreferenceName);
        PresetModesLabel.Text = Loc.GetString(
            "rmc-ui-options-announcements-layout-preset-modes-value",
            ("modes", string.Join(", ", names)));
    }

    private void UpdateAppearanceControlState(AnnouncementDisplayData display)
    {
        ShowPortraitCheckBox.Disabled = !display.SupportsSpriteCardOverride;

        var showSpriteBox = display.SupportsSpriteCardOverride && display.ShowSprite;
        var showCRT = display.Style.AnimationConfig.EnableCRT;
        var showBg = display.Style.BackgroundConfig.ShowBackground;

        SpriteBoxColorRow.Visible = showSpriteBox;
        CRTGlowColorSection.Visible = showCRT;
        BackgroundColorSection.Visible = showBg;
        ExtraColorRow.Visible = showCRT || showBg;
    }

    private void RefreshColorPickers(AnnouncementDisplayData display)
    {
        var bodyColor = display.TextColorOverride ?? display.Style.TextConfig.PrimaryColor;
        var titleColor = display.TitleColorOverride ?? display.Style.TitleConfig.TitleColor;
        var spriteBoxColor = display.SpriteBoxColorOverride ?? display.Style.SpriteConfig.SpriteBoxColor;
        var spriteBoxBorderColor = display.SpriteBoxBorderColorOverride ?? display.Style.SpriteConfig.SpriteBoxBorderColor;
        var bgColor = display.BackgroundColorOverride ?? display.Style.BackgroundConfig.BackgroundColor;

        var crtGlowColor = display.CRTGlowColorOverride
            ?? display.Style.AnimationConfig.CRTSettings?.GlowColor
            ?? Color.FromHex("#00ff41");

        _suppressTextChange = true;
        BodyColorPicker.Color = bodyColor;
        TitleColorPicker.Color = titleColor;
        SpriteBoxColorPicker.Color = spriteBoxColor;
        SpriteBoxBorderColorPicker.Color = spriteBoxBorderColor;
        CRTGlowColorPicker.Color = crtGlowColor;
        BackgroundColorPicker.Color = bgColor;
        _suppressTextChange = false;
    }

    private AnnouncementLayoutOverride? GetEffectiveEditedLayout()
    {
        return GetStoredEditedLayout() ?? _announcementController.GetGlobalLayoutOverride();
    }

    private AnnouncementLayoutOverride? GetStoredEditedLayout()
    {
        return _announcementController.GetPresetLayoutOverride(_selectedPreset);
    }

    private Vector2 GetPreviewPositionOrDefault()
    {
        if (_layoutController.CurrentPosition is { } current)
            return current;

        if (GetEffectiveEditedLayout() is { } layout)
            return layout.ScreenPosition;

        return new Vector2(0.5f, 0.18f);
    }

    private void SaveEditedLayout(AnnouncementLayoutOverride layout)
    {
        var overrides = AnnouncementLayoutOverrides.Parse(_cfg.GetCVar(RMCCVars.RMCAnnouncementLayoutOverrides));
        overrides[_selectedPreset.ToString()] = layout;
        _cfg.SetCVar(RMCCVars.RMCAnnouncementLayoutOverrides, AnnouncementLayoutOverrides.Serialize(overrides));
    }

    private void ResetEditedLayout()
    {
        var overrides = AnnouncementLayoutOverrides.Parse(_cfg.GetCVar(RMCCVars.RMCAnnouncementLayoutOverrides));
        overrides.Remove(_selectedPreset.ToString());
        _cfg.SetCVar(RMCCVars.RMCAnnouncementLayoutOverrides, AnnouncementLayoutOverrides.Serialize(overrides));
    }

    private static string[] GetPreviewBody(AnnouncementPresetPrototype preset)
    {
        return
        [
            Loc.GetString("rmc-ui-options-announcements-layout-preview-line-1", ("name", preset.Name)),
            Loc.GetString("rmc-ui-options-announcements-layout-preview-line-2")
        ];
    }

    private string ResolvePreviewSpeakerName()
    {
        if (_player.LocalEntity is { } localEntity &&
            _entityManager.TryGetComponent(localEntity, out MetaDataComponent? metaData) &&
            !string.IsNullOrWhiteSpace(metaData.EntityName))
        {
            return metaData.EntityName;
        }

        return Loc.GetString("rmc-ui-options-announcements-layout-preview-speaker");
    }

    private void OnControllerPreviewPositionChanged(Vector2 position)
    {
        var layout = CreateEditedLayout(position, ScaleSlider.Value);
        SaveEditedLayout(layout);
        RefreshValueLabels(layout, true);
    }

    private void OnControllerPreviewScaleChanged(float scale)
    {
        _suppressSliderChange = true;
        ScaleSlider.SetValueWithoutEvent(scale);
        _suppressSliderChange = false;

        var layout = CreateEditedLayout(GetPreviewPositionOrDefault(), scale);
        SaveEditedLayout(layout);
        RefreshValueLabels(layout, true);
    }
}
