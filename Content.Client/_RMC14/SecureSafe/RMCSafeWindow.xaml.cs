using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Timing;

namespace Content.Client._RMC14.SecureSafe;

[GenerateTypedNameReferences]
public sealed partial class RMCSafeWindow : DefaultWindow
{
    private readonly IGameTiming _timing;
    private TimeSpan _lastClickTime;

    public RMCSafeWindow()
    {
        RobustXamlLoader.Load(this);
        _timing = IoCManager.Resolve<IGameTiming>();
    }

    public void UpdateState(int dial1, int dial2)
    {
        Dial1Label.Text = dial1.ToString("D2");
        Dial2Label.Text = dial2.ToString("D2");
    }

    // A helper method to prevent spamming the dial too fast.
    public bool CheckCooldown()
    {
        if (_timing.CurTime - _lastClickTime < TimeSpan.FromSeconds(0.1))
            return false;

        _lastClickTime = _timing.CurTime;
        return true;
    }
}
