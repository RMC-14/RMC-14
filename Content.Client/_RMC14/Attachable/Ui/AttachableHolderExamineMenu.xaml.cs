using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Content.Client.UserInterface.Controls;

namespace Content.Client._RMC14.Attachable.Ui;

[GenerateTypedNameReferences]
public sealed partial class AttachableHolderExamineMenu : FancyWindow
{
    public AttachableHolderExamineMenu()
    {
        RobustXamlLoader.Load(this);
    }

    public void UpdateSlots(Dictionary<string, List<string>> slots)
    {
        SlotsContainer.RemoveAllChildren();

        if (slots.Count == 0)
        {
            SlotsContainer.AddChild(new Label
            {
                Text = Loc.GetString("rmc-attachable-examine-no-slots"),
                HorizontalAlignment = HAlignment.Center,
            });
            return;
        }

        foreach (var (slotName, entries) in slots)
        {
            SlotsContainer.AddChild(new Label
            {
                Text = slotName,
                StyleClasses = { "LabelHeading" },
            });

            foreach (var entry in entries)
            {
                SlotsContainer.AddChild(new Label
                {
                    Text = $"  • {entry}",
                    Margin = new Thickness(4, 0, 0, 2),
                });
            }
        }
    }
}
