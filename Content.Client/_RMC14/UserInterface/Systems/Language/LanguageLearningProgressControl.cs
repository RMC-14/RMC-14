namespace Content.Client._RMC14.UserInterface.Systems.Language;

[GenerateTypedNameReferences]
public sealed partial class LanguageLearningProgressControl : Control
{
    [Dependency] private readonly IEntitySystemManager _entitySystemManager = default!;

    private const float EstimatedChipWidth = 90f;
    private const int MaxWordsPerRow = 10;
    private const int MinWordsPerRow = 2;

    private static readonly Color[] ComprehensionBackgroundColors =
    {
        Color.FromHex("#1F1B1B"),
        Color.FromHex("#2F1B1B"),
        Color.FromHex("#2F251B"),
        Color.FromHex("#2F2F1B"),
        Color.FromHex("#1B2F1B")
    };

    private static readonly Color[] ComprehensionColors =
    {
        Color.DarkRed,
        Color.Red,
        Color.Orange,
        Color.Yellow,
        Color.LightGreen
    };

    private readonly StyleBoxFlat _headerStyle = new()
    {
        BackgroundColor = Color.FromHex("#1B1B1E"),
        BorderColor = Color.FromHex("#404040"),
        BorderThickness = new Thickness(1),
        ContentMarginTopOverride = 8,
        ContentMarginBottomOverride = 8,
        ContentMarginLeftOverride = 8,
        ContentMarginRightOverride = 8
    };

    private readonly StyleBoxFlat _wordsStyle = new()
    {
        BackgroundColor = Color.FromHex("#2A2A2E"),
        BorderColor = Color.FromHex("#404040"),
        BorderThickness = new Thickness(1, 0, 1, 1),
        ContentMarginTopOverride = 8,
        ContentMarginBottomOverride = 8,
        ContentMarginLeftOverride = 8,
        ContentMarginRightOverride = 8
    };

    private List<WordEntry>? _filteredWords;
    private LanguageLearningViewData _data;
    private LanguagePrototype _prototype = default!;
    private bool _wordsExpanded;
    private string _searchFilter = string.Empty;

    public ProtoId<LanguagePrototype> LanguageId => _prototype.ID;
    public event Action<ProtoId<LanguagePrototype>, bool>? OnExpansionChanged;

    public LanguageLearningProgressControl()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        HorizontalExpand = true;
        HeaderPanel.PanelOverride = _headerStyle;
        WordsPanel.PanelOverride = _wordsStyle;
        SearchBox.PlaceHolder = Loc.GetString("language-learning-search-placeholder");

        ExpandButton.OnPressed += _ => SetExpanded(!_wordsExpanded);
        SearchBox.OnTextChanged += OnSearchTextChanged;
    }

    public LanguageLearningProgressControl(LanguagePrototype prototype, LanguageLearningViewData data) : this()
    {
        UpdateData(prototype, data);
    }

    public void UpdateData(LanguagePrototype prototype, LanguageLearningViewData data)
    {
        _prototype = prototype;
        _data = data;
        _filteredWords = null;
        _searchFilter = string.Empty;

        NameLabel.Text = prototype.LocalizedName;
        SearchBox.Text = string.Empty;
        LoadLanguageIcon();
        UpdateDisplayedData();

        if (_wordsExpanded)
            RebuildWordsDisplay();
    }

    public void SetExpanded(bool expanded)
    {
        if (_wordsExpanded == expanded)
            return;

        _wordsExpanded = expanded;
        UpdateExpansionState();
        OnExpansionChanged?.Invoke(LanguageId, _wordsExpanded);
    }

    protected override void Resized()
    {
        base.Resized();

        if (_wordsExpanded && _filteredWords?.Count > 0)
            BuildWordsDisplay();
    }

    private int CalculateWordsPerRow()
    {
        var containerWidth = WordsContainer.Size.X > 0 ? WordsContainer.Size.X : WordsPanel.Size.X;
        if (containerWidth <= 0)
            containerWidth = 300f;

        var availableWidth = Math.Max(containerWidth - 16, 150);
        var wordsPerRow = Math.Max(1, (int) (availableWidth / EstimatedChipWidth));
        return Math.Clamp(wordsPerRow, MinWordsPerRow, MaxWordsPerRow);
    }

    private static BoxContainer CreateNewRow()
    {
        return new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            HorizontalExpand = true,
            SeparationOverride = 2
        };
    }

    private WordEntry CreateWordEntry(string word, float comprehension, int relevance)
    {
        var displayWord = _entitySystemManager.GetEntitySystem<LanguageLearningSystem>().ProcessWordForDisplay(
            word,
            LanguageId,
            comprehension,
            _data.Progress);

        return new WordEntry(word, displayWord, comprehension, relevance);
    }

    private static Control CreateWordChip(string word, float comprehension)
    {
        var colorIndex = GetComprehensionColorIndex(comprehension);
        var wordChip = new PanelContainer
        {
            PanelOverride = new StyleBoxFlat
            {
                BackgroundColor = ComprehensionBackgroundColors[colorIndex],
                BorderColor = ComprehensionColors[colorIndex],
                BorderThickness = new Thickness(1),
                ContentMarginTopOverride = 1,
                ContentMarginBottomOverride = 1,
                ContentMarginLeftOverride = 4,
                ContentMarginRightOverride = 4
            }
        };

        var wordContainer = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            VerticalAlignment = VAlignment.Center
        };

        var wordLabel = new Label
        {
            Text = word,
            FontColorOverride = ComprehensionColors[colorIndex],
            VerticalAlignment = VAlignment.Center,
            Margin = new Thickness(0, 0, 2, 0)
        };

        var comprehensionLabel = new Label
        {
            Text = comprehension.ToString("P0"),
            FontColorOverride = ComprehensionColors[colorIndex],
            VerticalAlignment = VAlignment.Center,
            StyleClasses = { "LabelSubText" }
        };

        wordContainer.AddChild(wordLabel);
        wordContainer.AddChild(comprehensionLabel);
        wordChip.AddChild(wordContainer);
        return wordChip;
    }

    private void BuildWordsDisplay()
    {
        WordsContainer.RemoveAllChildren();

        if (_filteredWords == null)
            return;

        if (_filteredWords.Count == 0)
        {
            ShowNoResultsMessage();
            return;
        }

        var wordsPerRow = CalculateWordsPerRow();
        var currentRow = CreateNewRow();
        var wordsInCurrentRow = 0;

        foreach (var entry in _filteredWords)
        {
            if (wordsInCurrentRow >= wordsPerRow)
            {
                WordsContainer.AddChild(currentRow);
                currentRow = CreateNewRow();
                wordsInCurrentRow = 0;
            }

            currentRow.AddChild(CreateWordChip(entry.DisplayWord, entry.Comprehension));
            wordsInCurrentRow++;
        }

        if (wordsInCurrentRow > 0)
            WordsContainer.AddChild(currentRow);
    }

    private void FilterAndDisplayWords()
    {
        if (_data.WordCount == 0)
            return;

        FilterWords();
        BuildWordsDisplay();
    }

    private void FilterWords()
    {
        var searchTerms = GetSearchTerms(_searchFilter);
        if (searchTerms.Length == 0)
        {
            _filteredWords = _data.LearnedWords
                .Select(kvp => CreateWordEntry(kvp.Key, kvp.Value, 1))
                .OrderByDescending(entry => entry.Comprehension)
                .ThenBy(entry => entry.Word)
                .ToList();
            return;
        }

        var exactMatches = new List<WordEntry>();
        var otherMatches = new List<WordEntry>();

        for (var i = 0; i < searchTerms.Length; i++)
        {
            var term = searchTerms[i];
            if (_data.LearnedWords.TryGetValue(term, out var comprehension))
                exactMatches.Add(CreateWordEntry(term, comprehension, 1000 + (searchTerms.Length - i)));
        }

        foreach (var (word, comprehension) in _data.LearnedWords)
        {
            if (searchTerms.Contains(word.ToLowerInvariant()))
                continue;

            var relevance = CalculateSearchRelevance(word, searchTerms);
            if (relevance > 0)
                otherMatches.Add(CreateWordEntry(word, comprehension, relevance));
        }

        _filteredWords = exactMatches
            .Concat(otherMatches
                .OrderByDescending(entry => entry.Relevance)
                .ThenByDescending(entry => entry.Comprehension)
                .ThenBy(entry => entry.Word))
            .ToList();
    }

    private static int CalculateSearchRelevance(string word, string[] searchTerms)
    {
        if (searchTerms.Length == 0)
            return 1;

        var wordLower = word.ToLowerInvariant();
        var relevance = 0;

        foreach (var term in searchTerms)
        {
            relevance += wordLower switch
            {
                var value when value == term => 1000,
                var value when value.StartsWith(term) => 500,
                var value when value.EndsWith(term) => 300,
                var value when value.Contains(term) => 100 + (word.Length <= 10 ? 50 : 0),
                _ => 0
            };
        }

        return relevance;
    }

    private static int GetComprehensionColorIndex(float comprehension)
    {
        return comprehension switch
        {
            >= 0.8f => 4,
            >= 0.6f => 3,
            >= 0.4f => 2,
            >= 0.2f => 1,
            _ => 0
        };
    }

    private static string[] GetSearchTerms(string searchFilter)
    {
        return string.IsNullOrWhiteSpace(searchFilter)
            ? []
            : searchFilter.ToLowerInvariant().Split([' ', '\t'], StringSplitOptions.RemoveEmptyEntries);
    }

    private void LoadLanguageIcon()
    {
        LanguageIcon.Texture = null;
        LanguageIcon.Visible = false;

        var spriteSystem = _entitySystemManager.GetEntitySystem<SpriteSystem>();
        if (_prototype.LanguageIcon is not { } icon)
            return;

        LanguageIcon.Texture = spriteSystem.Frame0(icon);
        LanguageIcon.Visible = true;
    }

    private void OnSearchTextChanged(LineEdit.LineEditEventArgs args)
    {
        _searchFilter = args.Text.Trim();
        FilterAndDisplayWords();
    }

    private void RebuildWordsDisplay()
    {
        _filteredWords = null;
        FilterAndDisplayWords();
    }

    private void ShowNoResultsMessage()
    {
        WordsContainer.AddChild(new Label
        {
            Text = Loc.GetString("language-learning-no-results"),
            FontColorOverride = Color.Gray,
            StyleClasses = { "LabelSubText" },
            Margin = new Thickness(0, 4, 0, 0)
        });
    }

    private void UpdateDisplayedData()
    {
        var overallProgress = Loc.GetString("language-learning-overall-progress", ("progress", _data.Progress.ToString("P1")));
        var wordCount = Loc.GetString("language-learning-word-count", ("count", _data.WordCount));
        var averageComprehension = Loc.GetString(
            "language-learning-average-comprehension",
            ("average", _data.AverageWordComprehension.ToString("P1")));

        StatsLabel.Text = string.Join(" | ", overallProgress, wordCount, averageComprehension);
        ProgressBar.Value = Math.Clamp(_data.Progress, 0f, 1f);
        UpdateExpandButtonVisibility();
    }

    private void UpdateExpandButtonVisibility()
    {
        var hasLearnedWords = _data.WordCount > 0;
        ExpandButton.Visible = hasLearnedWords;

        if (!hasLearnedWords)
            _wordsExpanded = false;

        UpdateExpansionState();
    }

    private void UpdateExpansionState()
    {
        WordsPanel.Visible = _wordsExpanded && _data.WordCount > 0;
        ExpandButton.ToolTip = Loc.GetString(_wordsExpanded
            ? "language-learning-hide-words"
            : "language-learning-show-words");
        ExpandButton.Modulate = _wordsExpanded
            ? Color.White
            : Color.FromHex("#888888");

        if (WordsPanel.Visible)
            RebuildWordsDisplay();
    }

    private readonly record struct WordEntry(string Word, string DisplayWord, float Comprehension, int Relevance);
}
