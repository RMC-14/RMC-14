using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._RMC14.Marines.GroundsideOperations;

[GenerateTypedNameReferences]
public sealed partial class GroundsideOperationsIconButton : ContainerButton
{
    private readonly StyleBoxFlat _style = new()
    {
        BackgroundColor = Color.FromHex("#79C5EC"),
        BorderColor = Color.FromHex("#8CD8FF"),
        BorderThickness = new Thickness(1),
    };

    public string? Text
    {
        get => TextLabel.Text;
        set => TextLabel.Text = value;
    }

    public GroundsideOperationsIcon Icon
    {
        get => IconControl.Icon;
        set => IconControl.Icon = value;
    }

    public bool Navigation
    {
        set
        {
            Layout.HorizontalExpand = value;
            Layout.HorizontalAlignment = value ? HAlignment.Left : HAlignment.Center;
            TextLabel.HorizontalExpand = value;
        }
    }

    public GroundsideOperationsIconButton()
    {
        RobustXamlLoader.Load(this);
        Background.PanelOverride = _style;
        SetPalette(false, false);
    }

    public void SetPalette(bool selected, bool danger, bool disabled = false)
    {
        var background = danger ? "#B72B24" : selected ? "#79C5EC" : "#071321";
        var foreground = disabled ? "#557080" : selected || danger ? "#020711" : "#D9F3FF";
        var border = danger ? "#E85A50" : "#78C8F2";
        _style.BackgroundColor = Color.FromHex(background);
        _style.BorderColor = Color.FromHex(border);
        IconControl.IconColor = Color.FromHex(foreground);
        TextLabel.FontColorOverride = Color.FromHex(foreground);
    }
}

public enum GroundsideOperationsIcon
{
    Cog = 0xF013,
    Home = 0xF015,
    Ban = 0xF05E,
    Warning = 0xF071,
    Bullhorn = 0xF0A1,
    Users = 0xF0C0,
    PaperPlane = 0xF1D8,
    Heartbeat = 0xF21E,
    Map = 0xF279,
    IdCard = 0xF2C2,
    DoorOpen = 0xF52B,
    Medal = 0xF5A2,
}
