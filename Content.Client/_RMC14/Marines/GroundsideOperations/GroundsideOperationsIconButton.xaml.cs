using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.ResourceManagement;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.IoC;
using Robust.Shared.Utility;

namespace Content.Client._RMC14.Marines.GroundsideOperations;

[GenerateTypedNameReferences]
public sealed partial class GroundsideOperationsIconButton : ContainerButton
{
    private static readonly ResPath IconRsiPath = new("/Textures/_RMC14/Interface/groundside_operations_icons.rsi");

    private readonly RSI _icons;
    private readonly StyleBoxFlat _style = new()
    {
        BackgroundColor = Color.FromHex("#79C5EC"),
        BorderColor = Color.FromHex("#8CD8FF"),
        BorderThickness = new Thickness(1),
    };
    private bool _danger;
    private bool _filled;
    private bool _navigation;
    private bool _paletteDisabled;
    private bool _selected;
    private GroundsideOperationsIcon _icon;

    public string? Text
    {
        get => TextLabel.Text;
        set => TextLabel.Text = value;
    }

    public GroundsideOperationsIcon Icon
    {
        get => _icon;
        set
        {
            _icon = value;
            IconControl.Texture = _icons[GetState(value)].Frame0;
        }
    }

    public bool Navigation
    {
        get => _navigation;
        set
        {
            _navigation = value;
            Layout.HorizontalExpand = false;
            Layout.HorizontalAlignment = HAlignment.Center;
            TextLabel.HorizontalExpand = false;
            UpdatePalette();
        }
    }

    public bool Filled
    {
        get => _filled;
        set
        {
            _filled = value;
            UpdatePalette();
        }
    }

    public GroundsideOperationsIconButton()
    {
        _icons = IoCManager.Resolve<IResourceCache>().GetResource<RSIResource>(IconRsiPath).RSI;
        RobustXamlLoader.Load(this);
        Layout.HorizontalExpand = false;
        Layout.HorizontalAlignment = HAlignment.Center;
        TextLabel.HorizontalExpand = false;
        Background.PanelOverride = _style;
        SetPalette(false, false);
    }

    public void SetPalette(bool selected, bool danger, bool disabled = false)
    {
        _selected = selected;
        _danger = danger;
        _paletteDisabled = disabled;
        UpdatePalette();
    }

    protected override void DrawModeChanged()
    {
        base.DrawModeChanged();

        if (NameScope != null)
            UpdatePalette();
    }

    private void UpdatePalette()
    {
        var pressed = DrawMode == DrawModeEnum.Pressed;
        var hovered = DrawMode == DrawModeEnum.Hover;

        string background;
        string border;
        string foreground;
        if (_paletteDisabled)
        {
            background = "#00000F80";
            border = "#46677F";
            foreground = "#55768D";
        }
        else if (_danger)
        {
            background = pressed ? "#8E1712" : hovered ? "#E13B32" : "#C3251E";
            border = hovered ? "#FF766E" : "#F04B43";
            foreground = "#00000F";
        }
        else if (_selected || _filled)
        {
            background = pressed ? "#5897C2" : hovered ? "#A9DDFF" : "#82C5F2";
            border = hovered ? "#D1EDFF" : "#8ACBFF";
            foreground = "#00000F";
        }
        else
        {
            background = pressed ? "#284C6680" : hovered ? "#8ACBFF4D" : "#00000F80";
            border = hovered ? "#B9E2FF" : "#8ACBFF";
            foreground = hovered ? "#FFFFFF" : "#8ACBFF";
        }

        _style.BackgroundColor = Color.FromHex(background);
        _style.BorderColor = Color.FromHex(border);
        IconControl.ModulateSelfOverride = Color.FromHex(foreground);
        TextLabel.FontColorOverride = Color.FromHex(foreground);
    }

    private static string GetState(GroundsideOperationsIcon icon)
    {
        return icon switch
        {
            GroundsideOperationsIcon.Ban => "ban",
            GroundsideOperationsIcon.Bullhorn => "bullhorn",
            GroundsideOperationsIcon.Cog => "cog",
            GroundsideOperationsIcon.DoorOpen => "door_open",
            GroundsideOperationsIcon.Heartbeat => "heartbeat",
            GroundsideOperationsIcon.Home => "home",
            GroundsideOperationsIcon.IdCard => "id_card",
            GroundsideOperationsIcon.Map => "map",
            GroundsideOperationsIcon.Medal => "medal",
            GroundsideOperationsIcon.PaperPlane => "paper_plane",
            GroundsideOperationsIcon.Users => "users",
            GroundsideOperationsIcon.Warning => "warning",
            _ => "ban",
        };
    }
}

public enum GroundsideOperationsIcon
{
    Ban,
    Bullhorn,
    Cog,
    DoorOpen,
    Heartbeat,
    Home,
    IdCard,
    Map,
    Medal,
    PaperPlane,
    Users,
    Warning,
}
