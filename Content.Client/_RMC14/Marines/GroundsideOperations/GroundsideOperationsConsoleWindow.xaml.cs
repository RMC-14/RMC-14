using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Timing;
using Robust.Shared.Utility;

namespace Content.Client._RMC14.Marines.GroundsideOperations;

[GenerateTypedNameReferences]
public sealed partial class GroundsideOperationsConsoleWindow : DefaultWindow
{
    public event Action? OnTimeRefresh;

    private float _timeRefreshAccumulator;

    public GroundsideOperationsConsoleWindow()
    {
        RobustXamlLoader.Load(this);
        GroundAnnouncementText.Placeholder = new Rope.Leaf(Loc.GetString("rmc-goc-groundside-placeholder"));

        ExecutiveNavigationButton.OnPressed += _ => ShowPage(GroundsideOperationsPage.Executive);
        EmergencyNavigationButton.OnPressed += _ => ShowPage(GroundsideOperationsPage.Emergency);
        OrdnanceNavigationButton.OnPressed += _ => ShowPage(GroundsideOperationsPage.Ordnance);

        OpenOverwatchButton.SetPalette(false, false);
        KeycardOverrideButton.SetPalette(false, false, true);
        ShowPage(GroundsideOperationsPage.Executive);
    }

    public void SetButtonState(
        GroundsideOperationsIconButton button,
        bool disabled,
        bool danger = false,
        GroundsideOperationsIcon? icon = null)
    {
        button.Disabled = disabled;
        button.SetPalette(false, danger, disabled);
        if (icon != null)
            button.Icon = icon.Value;
    }

    private void ShowPage(GroundsideOperationsPage page)
    {
        ExecutivePage.Visible = page == GroundsideOperationsPage.Executive;
        EmergencyPage.Visible = page == GroundsideOperationsPage.Emergency;
        OrdnancePage.Visible = page == GroundsideOperationsPage.Ordnance;

        StyleNavigationButton(ExecutiveNavigationButton, page == GroundsideOperationsPage.Executive);
        StyleNavigationButton(EmergencyNavigationButton, page == GroundsideOperationsPage.Emergency);
        StyleNavigationButton(OrdnanceNavigationButton, page == GroundsideOperationsPage.Ordnance);
    }

    private static void StyleNavigationButton(GroundsideOperationsIconButton button, bool selected)
    {
        button.Pressed = selected;
        button.SetPalette(selected, false);
    }

    protected override void FrameUpdate(FrameEventArgs args)
    {
        base.FrameUpdate(args);
        _timeRefreshAccumulator += args.DeltaSeconds;
        if (_timeRefreshAccumulator < 0.25f)
            return;

        _timeRefreshAccumulator = 0;
        OnTimeRefresh?.Invoke();
    }

    private enum GroundsideOperationsPage
    {
        Executive,
        Emergency,
        Ordnance,
    }
}
