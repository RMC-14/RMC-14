using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.ResourceManagement;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.IoC;
using Robust.Shared.Utility;

namespace Content.Client._RMC14.Marines.CommandTablet;

[GenerateTypedNameReferences]
public sealed partial class CommandTabletActionButton : ContainerButton
{
    private static readonly ResPath IconRsiPath = new("/Textures/_RMC14/Interface/groundside_operations_icons.rsi");

    private readonly RSI _icons;
    private readonly StyleBoxFlat _style = new()
    {
        BorderThickness = new Thickness(1),
    };
    private CommandTabletIcon _icon;

    public string? Text
    {
        get => TextLabel.Text;
        set => TextLabel.Text = value;
    }

    public CommandTabletIcon Icon
    {
        get => _icon;
        set
        {
            _icon = value;
            IconControl.Texture = _icons[GetState(value)].Frame0;
        }
    }

    public CommandTabletActionButton()
    {
        _icons = IoCManager.Resolve<IResourceCache>().GetResource<RSIResource>(IconRsiPath).RSI;
        RobustXamlLoader.Load(this);
        Layout.Align = BoxContainer.AlignMode.Begin;
        TextLabel.HorizontalExpand = false;
        Background.PanelOverride = _style;
        UpdatePalette();
    }

    protected override void DrawModeChanged()
    {
        base.DrawModeChanged();

        if (NameScope != null)
            UpdatePalette();
    }

    private void UpdatePalette()
    {
        var (background, border, foreground) = DrawMode switch
        {
            DrawModeEnum.Disabled => ("#8B8D90", "#A8AAAD", "#E6E6E6"),
            DrawModeEnum.Hover => ("#5D83B0", "#8DB9E8", "#FFFFFF"),
            DrawModeEnum.Pressed => ("#345778", "#77A7D7", "#FFFFFF"),
            _ => ("#456B96", "#668DB8", "#FFFFFF"),
        };

        _style.BackgroundColor = Color.FromHex(background);
        _style.BorderColor = Color.FromHex(border);
        IconControl.ModulateSelfOverride = Color.FromHex(foreground);
        TextLabel.FontColorOverride = Color.FromHex(foreground);
    }

    private static string GetState(CommandTabletIcon icon)
    {
        return icon switch
        {
            CommandTabletIcon.Ban => "ban",
            CommandTabletIcon.Bullhorn => "bullhorn",
            CommandTabletIcon.DoorOpen => "door_open",
            CommandTabletIcon.Map => "map",
            CommandTabletIcon.Medal => "medal",
            _ => "ban",
        };
    }
}

public enum CommandTabletIcon
{
    Ban,
    Bullhorn,
    DoorOpen,
    Map,
    Medal,
}
