using Content.Client.Resources;
using Robust.Client.AutoGenerated;
using Robust.Client.ResourceManagement;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.IoC;
using Robust.Shared.Timing;

namespace Content.Client._RMC14.Marines.CommandTablet;

[GenerateTypedNameReferences]
public sealed partial class CommandTabletWindow : DefaultWindow
{
    public event Action? OnTimeRefresh;

    private float _timeRefreshAccumulator;

    public CommandTabletWindow()
    {
        RobustXamlLoader.Load(this);

        var warningFont = IoCManager.Resolve<IResourceCache>()
            .GetFont("/Fonts/NotoSans/NotoSans-BoldItalic.ttf", 12);
        EvacuationWarningLineOne.FontOverride = warningFont;
        EvacuationWarningLineTwo.FontOverride = warningFont;
    }

    protected override void FrameUpdate(FrameEventArgs args)
    {
        base.FrameUpdate(args);
        _timeRefreshAccumulator += args.DeltaSeconds;
        if (_timeRefreshAccumulator < 0.25f)
            return;

        _timeRefreshAccumulator = 0;
        OnTimeRefresh?.Invoke();
    }
}
