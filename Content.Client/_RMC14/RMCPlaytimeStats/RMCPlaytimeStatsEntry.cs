using Content.Shared.Localizations;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;
using Robust.Shared.Timing;
using Robust.Client.GameObjects;
using Robust.Shared.Utility;

namespace Content.Client._RMC14.RMCPlaytimeStats;

[GenerateTypedNameReferences]
public sealed partial class RMCPlaytimeStatsEntry : ContainerButton
{
    public TimeSpan Playtime { get; private set; }

    public RMCPlaytimeStatsEntry(string role, TimeSpan playtime, StyleBox styleBox, SpriteSpecifier.Rsi? sprite)
    {
        RobustXamlLoader.Load(this);

        RoleLabel.Text = role;
        Playtime = playtime;
        PlaytimeLabel.Text = ContentLocalizationManager.FormatPlaytime(playtime);
        BackgroundColorPanel.PanelOverride = styleBox;

        var spriteSystem = IoCManager.Resolve<IEntitySystemManager>().GetEntitySystem<SpriteSystem>();
        var curTime = IoCManager.Resolve<IGameTiming>().CurTime;

        if (sprite is not null)
        {
            IconTexture.Texture = spriteSystem.GetFrame(sprite, curTime);
            IconTexture.Visible = true;
        }
    }

    public void UpdateShading(StyleBox styleBox)
    {
        BackgroundColorPanel.PanelOverride = styleBox;
    }

    public string? PlaytimeText => PlaytimeLabel.Text;

    public string? RoleText => RoleLabel.Text;
}
