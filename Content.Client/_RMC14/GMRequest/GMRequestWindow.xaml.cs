using Content.Client.UserInterface.Controls;
using Content.Shared._RMC14.CCVar;
using Content.Shared._RMC14.GMRequest;
using Robust.Client.AutoGenerated;
using Robust.Client.Console;
using Robust.Client.Graphics;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Configuration;

namespace Content.Client._RMC14.GMRequest;

[GenerateTypedNameReferences]
public sealed partial class GMRequestWindow : DefaultWindow
{
    [Dependency] private readonly GMRequestClientManager _manager = default!;
    [Dependency] private readonly IClientConsoleHost _clientConsole = default!;
    [Dependency] private readonly IConfigurationManager _cfg = default!;

    public GMRequestLog? _selectedLog;
    public int? _selectedLogId;

    public GMRequestWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        UpdateCVarButton();
        UpdateButtonsEnabled();
        UpdateMuteButton();
        Populate();

        LogsList.GenerateItem += GenerateButton;
        LogsList.ItemPressed += LogListItemPressed;
        LogsList.NoItemSelected += LogListNoItemSelected;

        //Hardcoding this is unideal, but styleclass and palettes are confusing
        LogsPanel.PanelOverride = new StyleBoxFlat { BackgroundColor = new Color(32, 32, 40) };

        ListsTab.OnTabChanged += ListTabPressed;

        ToggleGMRequestCVarButton.OnPressed += ToggleCVarButtonPressed;
        TeleportToPlayerButton.OnPressed += TeleportButtonPressed;
        AhelpPlayerButton.OnPressed += AhelpButtonPressed;
        MuteButton.OnPressed += MuteButtonPressed;

        _cfg.OnValueChanged(RMCCVars.RMCGMRequestEnabled, _ => UpdateCVarButton(), true);
        _cfg.OnValueChanged(RMCCVars.RMCGMRequestSoundMuted, _ => UpdateMuteButton(), true);
    }
    private void LogListNoItemSelected()
    {
        _selectedLog = null;
        UpdateButtonsEnabled();
        ClaimLogButton.Text = Loc.GetString("rmc-gm-request-window-button-claim");
    }

    private void LogListItemPressed(BaseButton.ButtonEventArgs? args, ListData? data)
    {
        if (args == null || data is not GMRequestLogEntryData { Log: var log, Id: var id})
            return;

        _selectedLog = log;
        _selectedLogId = id;

        ClaimLogButton.Text =
            _selectedLog.Value.ClaimName != null
                ? Loc.GetString("rmc-gm-request-window-button-unclaim")
                : Loc.GetString("rmc-gm-request-window-button-claim");

        UpdateButtonsEnabled();
    }

    public void Populate()
    {
        var entrydata = new List<GMRequestLogEntryData>();

        foreach (var log in _manager.Logs)
        {
            var entry = new GMRequestLogEntryData(log.Key, log.Value);

            //Only display Shown logs in the shown tab, vice versa
            if((ListsTab.CurrentTab == 0 && !entry.Log.Hidden) || (ListsTab.CurrentTab == 1 && entry.Log.Hidden))
                entrydata.Add(entry);
        }

        var sortedentrydata = new List<GMRequestLogEntryData>(entrydata);
            sortedentrydata.Sort(Compare);

        LogsList.PopulateList(sortedentrydata);
    }

    private void GenerateButton(ListData data, ListContainerButton button)
    {
        if (data is not GMRequestLogEntryData { Log: var log, Id: var id })
            return;

        var entry = new GMRequestWindowEntry(log);

        button.AddChild(entry);
        button.AddStyleClass(ListContainer.StyleClassListContainerButton);

        //TODO: Fix the entries being shifted 10 pixels right due to the StyleClass's inherent ContentMarginLeftOverride

        // This did not work
        // if (button.TryGetStyleProperty<StyleBoxTexture>(ListContainer.StyleClassListContainerButton, out var box))
        // {
        //     box.ContentMarginLeftOverride = 0;
        //     button.StyleBoxOverride = box;
        // }

        button.ToolTip = $"{log.EntityName}: {log.Message}";
    }

    private void ToggleCVarButtonPressed(BaseButton.ButtonEventArgs args)
    {
        _clientConsole.ExecuteCommand("togglegmrequest");
    }
    private void AhelpButtonPressed(BaseButton.ButtonEventArgs args)
    {
        if (_selectedLog == null)
            return;

        _clientConsole.ExecuteCommand($"openahelp \"{_selectedLog.Value.Sender}\"");
    }

    private void TeleportButtonPressed(BaseButton.ButtonEventArgs args)
    {
        if (_selectedLog == null)
            return;

        _clientConsole.ExecuteCommand($"tpto \"{_selectedLog.Value.SenderName}\"");
    }

    private void ListTabPressed(int tab)
    {
        Populate();
        HideShowButton.Text =
            ListsTab.CurrentTab == 0
                ? Loc.GetString("rmc-gm-request-window-button-hide")
                : Loc.GetString("rmc-gm-request-window-button-show");
    }

    private void MuteButtonPressed(BaseButton.ButtonEventArgs args)
    {
        var muted = _cfg.GetCVar(RMCCVars.RMCGMRequestSoundMuted);
        muted = !muted;
        _cfg.SetCVar(RMCCVars.RMCGMRequestSoundMuted, muted);

        UpdateMuteButton();
    }

    private void UpdateCVarButton()
    {
        var enabled = _cfg.GetCVar(RMCCVars.RMCGMRequestEnabled);
        CVarLabel.Text =
            enabled
                ? $"[color=lightgreen][bold]{Loc.GetString("rmc-gm-request-window-label-enabled")}"
                : $"[color=lightcoral][bold]{Loc.GetString("rmc-gm-request-window-label-disabled")}";

        ToggleGMRequestCVarButton.Pressed = enabled;
    }

    private void UpdateButtonsEnabled()
    {
        var selected = _selectedLog == null;

            ClaimLogButton.Disabled =  selected;
            TeleportToPlayerButton.Disabled = selected;
            SubtleMessagePlayerButton.Disabled = selected;
            AhelpPlayerButton.Disabled = selected;
            HideShowButton.Disabled = selected;
    }

    public void UpdateMuteButton()
    {
        MuteButton.Pressed = _cfg.GetCVar(RMCCVars.RMCGMRequestSoundMuted);
    }

    private int Compare(GMRequestLogEntryData x, GMRequestLogEntryData y)
    {
        //Sort by newest first
        return DateTime.Compare(y.Log.Time, x.Log.Time);
    }
}

public record GMRequestLogEntryData(int Id, GMRequestLog Log) : ListData;
