using System.Linq;
using System.Numerics;
using Content.Shared._RMC14.Language.Prototypes;
using Content.Shared._RMC14.Language.Components;
using Robust.Client.AutoGenerated;
using Robust.Client.ResourceManagement;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Client.Graphics;
using Robust.Shared.Prototypes;
using Robust.Shared.Utility;
using Robust.Shared.Log;

namespace Content.Client.UserInterface.Systems.Language;

[GenerateTypedNameReferences]
public sealed partial class LanguageMenuWindow : DefaultWindow
{
    [Dependency] private readonly IPrototypeManager _prototypeManager = default!;

    private string? _currentLanguage;
    private LanguageLearningComponent? _learningComponent;
    private Dictionary<string, bool> _expansionStates = new();

    private readonly StyleBoxFlat _mainContainerStyle = new()
    {
        BackgroundColor = Color.FromHex("#252525"),
        BorderColor = Color.FromHex("#1A1A1A"),
        BorderThickness = new Thickness(1)
    };

    private readonly StyleBoxFlat _contentContainerStyle = new()
    {
        BackgroundColor = Color.FromHex("#1B1B1E"),
        BorderColor = Color.FromHex("#404040"),
        BorderThickness = new Thickness(1)
    };

    private readonly StyleBoxFlat _tabContainerStyle = new()
    {
        BackgroundColor = Color.FromHex("#2A2A2E"),
        BorderColor = Color.FromHex("#404040"),
        BorderThickness = new Thickness(1)
    };

    public event Action<string>? OnLanguageSelected;

    public string? ThemeColor
    {
        get => MainContainer.ActualModulateSelf.ToHex();
        set
        {
            if (value != null)
            {
                var color = Color.FromHex(value);
                MainContainer.ModulateSelfOverride = color;
                ApplyThemeToComponents(color);
            }
        }
    }

    public string? AccentColor
    {
        get => TabContainer.ActualModulateSelf.ToHex();
        set
        {
            if (value != null)
            {
                var color = Color.FromHex(value);
                TabContainer.ModulateSelfOverride = color;
            }
        }
    }

    public LanguageMenuWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        SetupTabs();
        SetupStyling();
    }

    private void SetupTabs()
    {
        TabContainer.SetTabTitle(KnownLanguagesTab, Loc.GetString("language-menu-known-tab"));
        TabContainer.SetTabTitle(LearningTab, Loc.GetString("language-menu-learning-tab"));
    }

    private void SetupStyling()
    {
        MainContainer.PanelOverride = _mainContainerStyle;
        ContentContainer.PanelOverride = _contentContainerStyle;
        KnownLanguagesContainer.PanelOverride = _tabContainerStyle;
        LearningLanguagesContainer.PanelOverride = _tabContainerStyle;
    }

    private void ApplyThemeToComponents(Color themeColor)
    {
        var hsv = Color.ToHsv(themeColor);
        var darkerTheme = Color.FromHsv(hsv with { Z = hsv.Z * 0.8f });
        var lighterTheme = Color.FromHsv(hsv with { Z = Math.Min(1.0f, hsv.Z * 1.2f) });

        _contentContainerStyle.BackgroundColor = themeColor;
        _tabContainerStyle.BackgroundColor = darkerTheme;
    }

    public void UpdateLanguages(string? currentLanguage, HashSet<string> knownLanguages, LanguageLearningComponent? learningComponent)
    {
        Logger.Info("UpdateLanguages called");
        _currentLanguage = currentLanguage;
        _learningComponent = learningComponent;

        UpdateKnownLanguagesTab(knownLanguages);
        UpdateLearningTab();

        if (_learningComponent != null && _learningComponent.LearnableLanguages.Count > 0)
        {
            TabContainer.SetTabVisible(LearningTab, true);
        }
        else
        {
            TabContainer.SetTabVisible(LearningTab, false);
            TabContainer.CurrentTab = 0;
        }
    }

    private void UpdateKnownLanguagesTab(HashSet<string> knownLanguages)
    {
        KnownLanguageList.RemoveAllChildren();

        var sortedLanguages = knownLanguages
            .Where(lang => _prototypeManager.TryIndex<LanguagePrototype>(lang, out var proto) && proto.IsVisibleLanguage)
            .OrderBy(lang => _prototypeManager.Index<LanguagePrototype>(lang).LocalizedName)
            .ToList();

        foreach (var languageId in sortedLanguages)
        {
            if (!_prototypeManager.TryIndex<LanguagePrototype>(languageId, out var proto))
                continue;

            var button = new LanguageButton(proto, languageId == _currentLanguage);
            var capturedLanguageId = languageId;
            button.OnPressed += () => OnLanguageSelected?.Invoke(capturedLanguageId);
            KnownLanguageList.AddChild(button);
        }
    }

    private void UpdateLearningTab()
    {
        Logger.Info("UpdateLearningTab called");

        var existingControls = new Dictionary<string, LanguageLearningProgressControl>();
        foreach (var child in LearningLanguageList.Children.OfType<LanguageLearningProgressControl>())
        {
            existingControls[child.LanguageId] = child;
        }

        LearningLanguageList.RemoveAllChildren();

        if (_learningComponent == null)
        {
            Logger.Info("Learning component is null");
            return;
        }

        Logger.Info($"Learning component reference: {_learningComponent.GetHashCode()}");
        Logger.Info($"LearnableLanguages: {_learningComponent.LearnableLanguages.Count}");
        Logger.Info($"LanguageProgress: {_learningComponent.LanguageProgress.Count}");
        Logger.Info($"LearnedWords reference: {(_learningComponent.LearnedWords == null ? "NULL" : _learningComponent.LearnedWords.GetHashCode().ToString())}");
        Logger.Info($"LearnedWords count: {_learningComponent.LearnedWords?.Count ?? -1}");

        if (_learningComponent.LearnedWords != null)
        {
            Logger.Info($"LearnedWords keys: [{string.Join(", ", _learningComponent.LearnedWords.Keys)}]");
        }

        var sortedLearningLanguages = _learningComponent.LearnableLanguages
            .Where(lang => _prototypeManager.TryIndex<LanguagePrototype>(lang, out var proto) && proto.IsVisibleLanguage)
            .OrderBy(lang => _prototypeManager.Index<LanguagePrototype>(lang).LocalizedName)
            .ToList();

        Logger.Info($"Processing {sortedLearningLanguages.Count} learnable languages: [{string.Join(", ", sortedLearningLanguages)}]");

        foreach (var languageId in sortedLearningLanguages)
        {
            Logger.Info($"Processing language: {languageId}");

            if (!_prototypeManager.TryIndex<LanguagePrototype>(languageId, out var proto))
            {
                Logger.Info($"Could not find prototype for {languageId}");
                continue;
            }

            var learningProgress = _learningComponent.LanguageProgress.GetValueOrDefault(languageId, 0f);
            Logger.Info($"Learning progress for {languageId}: {learningProgress:P2}");

            Dictionary<string, float>? learnedWords = null;
            if (_learningComponent.LearnedWords?.TryGetValue(languageId, out var words) == true)
            {
                learnedWords = words;
                Logger.Info($"Successfully got words for {languageId}: {words.Count} words");
            }
            else
            {
                Logger.Info($"TryGetValue failed for {languageId}");
            }

            var wordCount = learnedWords?.Count ?? 0;
            var avgWordComprehension = learnedWords?.Values.Count > 0 ? learnedWords.Values.Average() : 0f;

            Logger.Info($"Final values for UI: progress={learningProgress:P2}, wordCount={wordCount}, avgComp={avgWordComprehension:P2}");

            LanguageLearningProgressControl progressControl;
            if (existingControls.TryGetValue(languageId, out var existingControl))
            {
                Logger.Info($"Reusing existing control for {languageId}");
                progressControl = existingControl;
                progressControl.UpdateData(proto, learningProgress, wordCount, avgWordComprehension, learnedWords);
            }
            else
            {
                Logger.Info($"Creating new control for {languageId}");
                var wasExpanded = _expansionStates.GetValueOrDefault(languageId, false);
                progressControl = new LanguageLearningProgressControl(proto, learningProgress, wordCount, avgWordComprehension, learnedWords, languageId);
                if (wasExpanded)
                {
                    progressControl.SetExpanded(true);
                }
                progressControl.OnExpansionChanged += (langId, expanded) => _expansionStates[langId] = expanded;
            }

            LearningLanguageList.AddChild(progressControl);
        }
    }
}
